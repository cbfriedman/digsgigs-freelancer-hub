// Helper to get all service slugs from industry specialties
export const getServiceSlug = (service: string): string => {
  return service
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
};

